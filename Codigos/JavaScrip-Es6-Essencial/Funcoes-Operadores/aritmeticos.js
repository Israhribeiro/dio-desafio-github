//Módulo (%)
//Operador binário. Retorna o inteiro restante da operação de divisão dos dois operandos

var x = 12 % 5 //retorna 2

//Incremento (++) e Decremento (--)
++x
x++

--x
x--

//Negação (-) e Adição (+)
x = -3
x = +'3' //Retorna 3
x = +true //Retorna 1
x = +false //Retorna 0
x = -true //Retorna -1

// Operador de exponenciação (**)
x = 2 ** 3 //Retorna 8
x = 10 ** -1 //Retorna 0.1

//Operador de agrupamento ()
x = 2 * (3 + 2)