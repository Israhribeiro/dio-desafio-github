//Igual (==)
//Retorna verdadeiro caso os operandos sejam iguais. -3 == var1
"3" == var1
3 == '3'

//Diferente (!=)
//Retorna verdadeiro caso os operandos sejam diferentes. -var1 != 4
var2 != '3'

//Estritamente igual (===)
//Retorna verdadeiro caso os operandos sejam iguais e do mesmo tipo
3 === var1

//Estritamente diferente (===)
//Retorna verdadeiro caso os operandos sejam diferentes e de tipos diferentes
3 !== var1

//Maior que (>)
//Retorna verdadeiro caso o operando da esquerda seja maior que o da direita
var2 > var1
"12" > 2

//Maior ou igual que (>=)
//Retorna verdadeiro caso o operando da esquerda seja maior ou igual que o da direita
var2 >= var1
var1 >= 3

//Menor que (<)
//Retorna verdadeiro caso o operando da esquerda seja menor que o da direita
var2 < var1
20 < 32

//Menor ou igual que (<=)
//Retorna verdadeiro caso o operando da esquerda seja menor ou igual que o da direita
20 <= var1
12 <= 50